# Değişkenler ve veri tipleri
sayi = 42  # int
isim = "123"  # str (bu örnekte sayısal bir string kullandık)
gercek_sayi = 3.14  # float
durum = True  # bool

# Veri tipi dönüşümleri
sayi_str = str(sayi)  # int to str
isim_int = int(isim)  # str to int (geçerli bir sayısal string)
gercek_sayi_int = int(gercek_sayi)  # float to int
durum_str = str(durum)  # bool to str

# Sonuçları yazdırma
print(f"sayi_str: {sayi_str}, isim_int: {isim_int}, gercek_sayi_int: {gercek_sayi_int}, durum_str: {durum_str}")
