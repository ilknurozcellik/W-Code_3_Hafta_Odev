# Yaş değişkenleri
ali_yas = 25
ayse_yas = 30
mehmet_yas = 20

# Karşılaştırmalar
print(ali_yas > ayse_yas)  # False
print(mehmet_yas < ali_yas)  # True

# Mantıksal operatörler ile karşılaştırmalar
print(ali_yas > mehmet_yas and ayse_yas > mehmet_yas)  # True
print(ali_yas < ayse_yas or mehmet_yas > ayse_yas)  # True
